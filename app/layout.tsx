import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";
import DevTokenSetter from "@/components/dev/DevTokenSetter";
import DevSessionSetter from "@/components/dev/DevSessionSetter";
import HeaderWrapper from "@/components/Header/HeaderWrapper";
import { getUserDetails } from "@/lib/getUserDetails";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "A Perfect PMS",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  console.log('Layout rendering, NODE_ENV:', process.env.NODE_ENV)
  
  // Fetch user details via proxy in development, direct in production
  const userUrl = process.env.NODE_ENV === 'development' 
    ? 'http://localhost:3000/api/proxy/user-details'
    : 'https://aperfectstay.ai/aps-api/v1/users/details/private'
  
  console.log('Fetching user from:', userUrl)
  
  const userResponse = await fetch(userUrl, { cache: 'no-store' })
  const user = userResponse.ok ? await userResponse.json() : null
  
  console.log('User fetched:', user ? 'Success' : 'Failed')
  
  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        <DevSessionSetter />
        <HeaderWrapper user={user} />
        {children}
        {/* <DevTokenSetter /> */}
      </body>
    </html>
  );
}
